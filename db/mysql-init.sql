CREATE TABLE app_user (
    id bigint NOT NULL,
    username varchar(50) NOT NULL,
    email varchar(255) NOT NULL,
    phone_number varchar(255),
    password_hint varchar(255),
    first_name varchar(50) NOT NULL,
