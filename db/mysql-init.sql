CREATE TABLE audit_event (
    audit_event_id int(11) NOT NULL AUTO_INCREMENT,
    date_created timestamp DEFAULT 0 NOT NULL,
    audit_event_type_cd int(11) NOT NULL,
    audit_event_description varchar(255),
    ref_person_id bigint COMMENT 'The field refers to a person record that is associated in some way with the audit event. For example in the case of a person record update audit event this field will refer to the person record that was updated.',
